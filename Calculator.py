num1 = float(input("Frist Number : "))
num2 = float(input("Second Number : "))
op = input("Select The Operation  + - * / : ")
   
if op == "+":
       print("Output:", num1 + num2)
elif op == "-":
       print("Output:", num1 - num2)
elif op == "*":
       print("Output:", num1 * num2)
elif op == "/":
       print("Output:", num1 / num2)
else:
       print("error ")
